//启动DMA接收
char UartRxData(UART_HandleTypeDef *huart, unsigned char *buf, const unsigned int len)
{
    HAL_StatusTypeDef status;
    char ret = TRUE;

    status = HAL_UART_Receive_DMA(huart, (unsigned char*)buf, len);
    if(HAL_OK != status)
    {
        ret = FALSE;
    }
    else
    {
        /* 开启空闲接收中断 */
        __HAL_UART_ENABLE_IT(huart, UART_IT_IDLE);
    }

    return ret;
}

//启动DMA接收
char StartUartRxDMA(UART_HandleTypeDef *huart)
{
    if(huart == &huart1)
        return UartRxData(huart, RxBuffer, UART_RX_BUF_SIZE);
    if(huart == &huart2)
        return UartRxData(huart, RxBuffer2, UART_RX_BUF_SIZE);
    return 0XFF;
}


//启动DMA发送
char UartTxData(UART_HandleTypeDef *huart, unsigned char *buf, const unsigned int len)
{
    HAL_StatusTypeDef status;
    char ret = TRUE;

    if(huart == &huart1)
    {
        if(sendCompleteSign == FALSE || len == 0)
        {
            return FALSE;
        }
        sendCompleteSign = FALSE;
    }
    if(huart == &huart2)
    {
        if(sendCompleteSign2 == FALSE || len == 0)
        {
            return FALSE;
        }
        sendCompleteSign2 = FALSE;
    }
    status = HAL_UART_Transmit_DMA(huart, (unsigned char*)buf, len);

    if(HAL_OK != status)
    {
        ret = FALSE;
    }

    return ret;
}

//启动DMA发送
char StartUartTxDMA(UART_HandleTypeDef *huart,uint8_t Len)
{
    if(huart == &huart1)
        return UartTxData(huart, TxBuffer, Len);
    if(huart == &huart2)
        return UartTxData(huart, TxBuffer2, Len);
    return 0XFF;
}

